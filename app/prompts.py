import random

def build_image_prompt(occasion_key: str, style_key: str, custom_occasion: str = "") -> str:
    """
    Генерирует разнообразный промпт для открытки с вариативностью формулировок.
    """
    if occasion_key == "custom" and custom_occasion:
        base = f"поздравительная композиция на тему «{custom_occasion}»"
    else:
        # Теперь IMAGE_DESCRIPTIONS содержит списки вариантов
        variants = IMAGE_DESCRIPTIONS.get(occasion_key, ["праздничная поздравительная композиция"])
        base = random.choice(variants) if isinstance(variants, list) else variants
    
    hint = STYLE_VISUAL_HINTS.get(style_key, "")
    
    # Вариативность вводных частей
    intro_variants = [
        "Создай красивую поздравительную открытку:",
        "Нарисуй праздничную открытку:",
        "Создай яркую поздравительную иллюстрацию:",
        "Нарисуй красивую открытку:",
        "Создай праздничную композицию:",
    ]
    
    # Вариативность требований к качеству
    quality_variants = [
        "Без текста и надписей. Высокое качество, 1024x1024.",
        "Только изображение, без букв. Профессиональное качество.",
        "Без надписей. Квадратный формат, яркие цвета.",
        "Визуальная композиция без текста. Современный стиль.",
        "Только картинка, без слов. Праздничное настроение.",
    ]
    
    intro = random.choice(intro_variants)
    quality = random.choice(quality_variants)
    
    return f"{intro} {base}. {hint}. {quality}"


