def generate_recipient_seed(recipient_name: str, occasion_key: str, salt: str = "") -> int:
    """
    Создаёт уникальный seed на основе имени получателя и повода.
    Обеспечивает стабильную вариативность — для одного получателя с одним поводом
    картинки будут в одной стилистике, но с разными вариациями при перегенерации.
    
    Args:
        recipient_name: имя получателя
        occasion_key: ключ повода
        salt: дополнительная соль (например, номер перегенерации)
    
    Returns:
        int: уникальный seed
    """
    hash_str = f"{recipient_name}_{occasion_key}_{salt}"
    return int(hashlib.md5(hash_str.encode()).hexdigest()[:8], 16)


